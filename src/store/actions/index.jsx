export function TraerTexto() {
  return async (dispatch) => {
    try {
      const json = await axios.get("../src/JSON/home.json");

      return dispatch({
        type: "TEXTO",
        data: json.data,
       
      });
    } catch (e) {
      console.log(e);
    }
  };
}

