export function TraerTexto() {
  return async (dispatch, getState) => {
    try {
      const json = await axios.get('https://jsonplaceholder.typicode.com/comments/4');

      return dispatch({
        type: "TEXTO",
        data: json.data,
       
      });
    } catch (e) {
      console.log(e);
    }
  };
}

